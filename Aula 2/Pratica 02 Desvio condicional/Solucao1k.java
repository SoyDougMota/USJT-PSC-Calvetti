public class Solucao1k {
    public static void main(String[] args) {
        int numero = 21;

        if (numero % 3 == 0 && numero % 7 == 0) {
            System.out.println("É divisível por 3 e por 7");
        } else {
            System.out.println("Não é divisível por 3 e por 7");
        }
    }
}
